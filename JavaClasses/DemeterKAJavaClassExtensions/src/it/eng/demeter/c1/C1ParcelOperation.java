package it.eng.demeter.c1;

public class C1ParcelOperation {
	
	private String id = "";
	private String parcelId = "";
	private String fertiliserTypeId = "";
	private String fertiliserType = "";
	private String quantity = "";

	public String getId() {
		return id;
	}
	public String getParcelId() {
		return parcelId;
	}
	public String getFertiliserTypeId() {
		return fertiliserTypeId;
	}
	public String getFertiliserType() {
		return fertiliserType;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setParcelId(String parcelId) {
		this.parcelId = parcelId;
	}
	public void setFertiliserTypeId(String fertiliserTypeId) {
		this.fertiliserTypeId = fertiliserTypeId;
	}
	public void setFertiliserType(String fertiliserType) {
		this.fertiliserType = fertiliserType;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	
}
