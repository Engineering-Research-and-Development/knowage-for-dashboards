package it.eng.demeter.a3;

public class A3FarmData {
	
	private String id = "";
	private String name = "";
	private String ndvi = "";
	
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getNdvi() {
		return ndvi;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setNdvi(String ndvi) {
		this.ndvi = ndvi;
	}
		
}
