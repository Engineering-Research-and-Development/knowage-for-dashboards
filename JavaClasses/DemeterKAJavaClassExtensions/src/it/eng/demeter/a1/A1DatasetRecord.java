package it.eng.demeter.a1;

public class A1DatasetRecord {
	private String area = "0";
	private String fieldId = "";
	private String realProduction = "0";
	private String predictedProduction = "0";
	private String predictionDate = "";
	private String avgPredictedProduction = "0";
	private String modelName = "";
	private String wkt = "";
	
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getFieldId() {
		return fieldId;
	}
	public void setFieldId(String fieldId) {
		this.fieldId = fieldId;
	}
	public String getRealProduction() {
		return realProduction;
	}
	public void setRealProduction(String realProduction) {
		this.realProduction = realProduction;
	}
	public String getPredictedProduction() {
		return predictedProduction;
	}
	public void setPredictedProduction(String predictedProduction) {
		this.predictedProduction = predictedProduction;
	}
	public String getPredictionDate() {
		return predictionDate;
	}
	public void setPredictionDate(String predictionDate) {
		this.predictionDate = predictionDate;
	}
	public String getAvgPredictedProduction() {
		return avgPredictedProduction;
	}
	public void setAvgPredictedProduction(String avgPredictedProduction) {
		this.avgPredictedProduction = avgPredictedProduction;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getWkt() {
		return wkt;
	}
	public void setWkt(String wkt) {
		this.wkt = wkt;
	}
	
	
}
