package it.eng.demeter.pilot5_2UC2;

public class UC2Animal {
	private String id = "";
	private String animalID = "";
	private String animalSpecies= "";
	private String earTag = "";
	private String birthdate = "";
	private String sex = "";
	private String breed = "";
	private String locatedAtFarm = "";
	private String healthCondition = "";
	private String isMemberOfAnimalGroup = "";
	private String notes = "";
	private String visibility = "";
	private String depiction = "";
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAnimalID() {
		return animalID;
	}
	public void setAnimalID(String animalID) {
		this.animalID = animalID;
	}
	public String getAnimalSpecies() {
		return animalSpecies;
	}
	public void setAnimalSpecies(String animalSpecies) {
		this.animalSpecies = animalSpecies;
	}
	public String getEarTag() {
		return earTag;
	}
	public void setEarTag(String earTag) {
		this.earTag = earTag;
	}
	public String getBirthdate() {
		return birthdate;
	}
	public void setBirthdate(String birthdate) {
		this.birthdate = birthdate;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getBreed() {
		return breed;
	}
	public void setBreed(String breed) {
		this.breed = breed;
	}
	public String getLocatedAtFarm() {
		return locatedAtFarm;
	}
	public void setLocatedAtFarm(String locatedAtFarm) {
		this.locatedAtFarm = locatedAtFarm;
	}
	public String getHealthCondition() {
		return healthCondition;
	}
	public void setHealthCondition(String healthCondition) {
		this.healthCondition = healthCondition;
	}
	public String getIsMemberOfAnimalGroup() {
		return isMemberOfAnimalGroup;
	}
	public void setIsMemberOfAnimalGroup(String isMemberOfAnimalGroup) {
		this.isMemberOfAnimalGroup = isMemberOfAnimalGroup;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
	public String getVisibility() {
		return visibility;
	}
	public void setVisibility(String visibility) {
		this.visibility = visibility;
	}
	public String getDepiction() {
		return depiction;
	}
	public void setDepiction(String depiction) {
		this.depiction = depiction;
	}
	
	
}
